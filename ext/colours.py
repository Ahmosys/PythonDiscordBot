class Colours:
    """
    Classe pour les couleurs, reprenant les guidelines d'Apple  (https://developer.apple.com/design/human-interface-guidelines/ios/visual-design/color/)
    """
    def blue():
        return (0x0A84FF)

    def brown():
        return (0xAC8E68)
    
    def cyan():
        return (0x64D2FF)
    
    def green():
        return (0x30D158)

    def indigo():
        return (0x5E5CE6)

    def mint():
        return (0x66D4CF)

    def orange():
        return (0xFF9F0A)

    def pink():
        return (0xFF375F)

    def purple():
        return (0xBF5AF2)

    def red():
        return (0xFF453A)

    def teal():
        return (0x40C8E0)

    def yellow():
        return (0xFFD60A)